#!/usr/bin/python3

class Square:
    """
    A class that represents a square.

    Attributes:
        __size (int): The size of the square (1 side).
    """

    def __init__(self, size):
        """
        Initializes a square with a given size.

        Args:
            size (int): The size of the square.
        """
        self.__size = size
